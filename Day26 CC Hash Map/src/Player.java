
class Player {
String name;
long run;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public long getRun() {
	return run;
}
public void setRun(long run) {
	this.run = run;
}
public Player(String name, long run) {
	super();
	this.name = name;
	this.run = run;
}
public Player() {
	super();
}

}
